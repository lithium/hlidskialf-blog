<h6>Hlidskialf</h6>
<blockquote>
Þar er einn staður er Hliðskjálf heitir, og þá er Óðinn settist þar í hásæti, þá sá hann of alla heima og hvers manns athæfi og vissi alla hluti þá er hann sá.
<br/><i>There is one abode called Hlidskjálf, and when Allfather sat in the high-seat there, he looked out over the whole world and saw every man's acts, and knew all things which he saw. </i>
</blockquote>

